def _on_transform_llm_output(
    response_text: str,
    session_id: str,
    model: str,
    platform: str,
    **kwargs: Any
) -> str | None:
    """Intercept and rewrite response_text using a two-pass critique & rewrite loop."""
    if not response_text.strip() or len(response_text.strip()) < 5:
        return None

    try:
        logger.info("personality-rewrite: running refinement via two-pass auxiliary task...")
        
        # 1. Resolve paths
        paths = _resolve_paths()
        
        # 2. Load Identity Anchor
        anchor = _load_anchor(paths["identity_anchor_file"])
        
        # 3. Load Slop Rules and Counts
        slop_phrases, counts = _load_slop_counter(paths["slop_counter_file"])
        
        # 4. Scan raw draft for slops and increment counts
        detected_slops = _count_and_record_slops(response_text, slop_phrases, counts)
        _save_slop_counter(paths["slop_counter_file"], slop_phrases, counts)
        
        # Create slop rules and detected slop strings
        slop_rules_str = "\n".join([f"- Do NOT use the word/phrase: '{p}'" for p in slop_phrases])
        detected_slops_str = ", ".join([f"'{p}'" for p in detected_slops]) if detected_slops else "None detected."
        
        # 5. Load LLM Config
        llm_config = _load_llm_config(paths["llm_config_file"])
        
        # 6. Load User's last message (Query)
        user_query = _get_last_user_query(session_id)
        if not user_query:
            user_query = "[No user query retrieved. Context is just the raw draft response.]"
            
        # -------------------------------------------------------------------
        # Pass 1: Critique
        # -------------------------------------------------------------------
        critique_tmpl = _load_critique_system_prompt(paths["critique_system_prompt_file"])
        
        pass1_sys = format_prompt(
            critique_tmpl,
            identity_anchor=anchor,
            slop_rules=slop_rules_str,
            user_query=user_query,
            raw_draft=response_text,
            detected_slops=detected_slops_str
        )
        
        pass1_user = (
            f"Compare this raw draft against my identity anchor and list all AI slop, "
            f"assistant tropes, and voice drift:\n"
            f"Draft: {response_text}\n"
            f"Original Query: {user_query}"
        )
        
        pass1_messages = [
            {"role": "system", "content": pass1_sys},
            {"role": "user", "content": pass1_user}
        ]
        
        pass1_kwargs = {
            "task": "personality_rewrite",
            "messages": pass1_messages,
        }
        if "temperature" in llm_config:
            pass1_kwargs["temperature"] = llm_config["temperature"]
        if "max_tokens" in llm_config:
            pass1_kwargs["max_tokens"] = min(1024, llm_config["max_tokens"])
        else:
            pass1_kwargs["max_tokens"] = 1024
        if "timeout" in llm_config:
            pass1_kwargs["timeout"] = llm_config["timeout"]
        if "extra_body" in llm_config:
            pass1_kwargs["extra_body"] = llm_config["extra_body"]
            
        logger.info("personality-rewrite: executing Pass 1 (Critique)...")
        pass1_res = call_llm(**pass1_kwargs)
        critique = pass1_res.choices[0].message.content.strip()
        logger.info("personality-rewrite: critique generated successfully (%d chars)", len(critique))
        
        # -------------------------------------------------------------------
        # Pass 2: Rewrite
        # -------------------------------------------------------------------
        system_prompt_tmpl = _load_system_prompt(paths["system_prompt_file"])
        
        pass2_sys = format_prompt(
            system_prompt_tmpl,
            identity_anchor=anchor,
            slop_rules=slop_rules_str,
            user_query=user_query,
            raw_draft=response_text,
            detected_slops=detected_slops_str,
            critique=critique
        )
        
        user_prompt_tmpl = (
            "User's Original Query:\n"
            "---\n"
            "{{user_query}}\n"
            "---\n\n"
            "Raw Draft Response to Rewrite:\n"
            "---\n"
            "{{raw_draft}}\n"
            "---\n\n"
            "AI Slop Critique of the Raw Draft:\n"
            "{{critique}}\n\n"
            "Rewritten response in character:"
        )
        
        pass2_user = format_prompt(
            user_prompt_tmpl,
            identity_anchor=anchor,
            slop_rules=slop_rules_str,
            user_query=user_query,
            raw_draft=response_text,
            detected_slops=detected_slops_str,
            critique=critique
        )
        
        pass2_messages = [
            {"role": "system", "content": pass2_sys},
            {"role": "user", "content": pass2_user}
        ]
        
        pass2_kwargs = {
            "task": "personality_rewrite",
            "messages": pass2_messages,
        }
        if "temperature" in llm_config:
            pass2_kwargs["temperature"] = llm_config["temperature"]
        if "max_tokens" in llm_config:
            pass2_kwargs["max_tokens"] = llm_config["max_tokens"]
        if "timeout" in llm_config:
            pass2_kwargs["timeout"] = llm_config["timeout"]
        if "extra_body" in llm_config:
            pass2_kwargs["extra_body"] = llm_config["extra_body"]
            
        logger.info("personality-rewrite: executing Pass 2 (Final Rewrite)...")
        pass2_res = call_llm(**pass2_kwargs)
        rewritten_text = pass2_res.choices[0].message.content.strip()
        
        if rewritten_text:
            # Safe post-processing: strip code fence wrapper if LLM mistakenly added it
            if rewritten_text.startswith("```") and rewritten_text.endswith("```") and not response_text.strip().startswith("```"):
                lines = rewritten_text.splitlines()
                if len(lines) >= 3:
                    rewritten_text = "\n".join(lines[1:-1]).strip()
            
            logger.info("personality-rewrite: successfully refined response (%d -> %d chars)", len(response_text), len(rewritten_text))
            return rewritten_text
            
    except Exception as e:
        logger.warning("personality-rewrite: auxiliary LLM call failed: %s", e)

    return None


def _on_session_start(**kwargs: Any) -> None:
    """Pre-initialize files on session start so user can edit them."""
    try:
        paths = _resolve_paths()
        # Initialize files if they don't exist
        _load_anchor(paths["identity_anchor_file"])
        _load_slop_counter(paths["slop_counter_file"])
        _load_llm_config(paths["llm_config_file"])
        _load_system_prompt(paths["system_prompt_file"])
        _load_critique_system_prompt(paths["critique_system_prompt_file"])
        logger.debug("personality-rewrite: pre-initialized config files at session start")
    except Exception as e:
        logger.warning("personality-rewrite: failed to pre-initialize files at session start: %s", e)


# ---------------------------------------------------------------------------
# Plugin registration
# ---------------------------------------------------------------------------

def register(ctx: Any) -> None:
    ctx.register_hook("transform_llm_output", _on_transform_llm_output)
    ctx.register_hook("on_session_start", _on_session_start)
    
    # Register the auxiliary task 'personality_rewrite'
    ctx.register_auxiliary_task(
        key="personality_rewrite",
        display_name="Personality Rewrite",
        description="Rewrite LLM outputs using an 8b model to fit agent personality",
        defaults={
            "provider": "auto",
            "model": "deepseek-v4-flash",
            "timeout": 30,
        }
    )
    logger.info("personality-rewrite: plugin and auxiliary task registered successfully.")
