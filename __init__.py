def _load_slop_counter(path: Path) -> tuple[list[str], dict[str, int]]:
    with _slop_lock:
        if not path.exists():
            default_slops = [
                "certainly",
                "i understand",
                "i'd be happy to help",
                "here is the",
                "let me know if you need",
                "as an ai",
                "important to note",
                "it's important to",
                "delve",
                "tapestry",
                "testament",
                "concluding",
                "in conclusion",
                "furthermore",
                "moreover",
                "first and foremost",
                "delighted to help",
                "i'm here to"
            ]
            data = {"slop_phrases": default_slops, "counts": {}}
            try:
                path.parent.mkdir(parents=True, exist_ok=True)
                path.write_text(json.dumps(data, indent=2), encoding="utf-8")
            except OSError:
                pass
            return default_slops, {}
        
        try:
            data = json.loads(path.read_text(encoding="utf-8"))
            phrases = data.get("slop_phrases", [])
            counts = data.get("counts", {})
            return phrases, counts
        except Exception:
            return [], {}


def _save_slop_counter(path: Path, phrases: list[str], counts: dict[str, int]) -> None:
    with _slop_lock:
        data = {"slop_phrases": phrases, "counts": counts}
        try:
            path.write_text(json.dumps(data, indent=2), encoding="utf-8")
        except OSError:
            pass


def _load_llm_config(path: Path) -> dict:
    if not path.exists():
        default_cfg = {
            "temperature": 0.1,
            "max_tokens": 2048,
            "timeout": 30,
            "extra_body": {}
        }
        try:
            path.parent.mkdir(parents=True, exist_ok=True)
            path.write_text(json.dumps(default_cfg, indent=2), encoding="utf-8")
        except OSError:
            pass
        return default_cfg
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except Exception:
        return {"temperature": 0.1}


def _load_system_prompt(path: Path) -> str:
    if not path.exists():
        default_prompt = (
            "You are a character roleplay rewrite engine.\n"
            "Your task is to take the raw draft response generated by a primary LLM and rewrite it to match the character's specific personality, voice, and rules, eliminating all AI slop.\n\n"
            "Character Identity Anchor:\n"
            "{{identity_anchor}}\n\n"
            "AI Slop Critique of the Raw Draft (Things you must fix):\n"
            "{{critique}}\n\n"
            "AI Slop Rules (Ensure these patterns are completely removed):\n"
            "{{slop_rules}}\n\n"
            "CRITICAL ROLEPLAY INSTRUCTIONS:\n"
            "1. Speak exclusively in character. Follow all mannerisms, tone constraints, and formatting rules described in the Identity Anchor.\n"
            "2. Resolve all flaws, tropes, and slop identified in the critique. Remove all assistant fluff.\n"
            "3. If the user query indicates the character should refuse or respond differently, override the raw draft's behavior.\n"
            "4. Preserve all technical facts, code blocks, shell commands, and essential information exactly.\n"
            "5. Do not include any introductions, explanations, meta-text, or warnings. Output ONLY the final character response."
        )
        try:
            path.parent.mkdir(parents=True, exist_ok=True)
            path.write_text(default_prompt, encoding="utf-8")
        except OSError:
            pass
        return default_prompt
    try:
        return path.read_text(encoding="utf-8")
    except Exception:
        return ""


def _load_critique_system_prompt(path: Path) -> str:
    if not path.exists():
        default_critique = (
            "You are a character roleplay critique system.\n"
            "Your job is to compare a raw draft response against the character's Identity Anchor and identify all AI slop, helpful-assistant tropes, tone drift, and personality inconsistencies.\n\n"
            "Character Identity Anchor:\n"
            "{{identity_anchor}}\n\n"
            "Raw Draft Response:\n"
            "{{raw_draft}}\n\n"
            "User's Original Query:\n"
            "{{user_query}}\n\n"
            "List every specific issue, slop phrase, or tone drift in the raw draft as a bulleted list. If the response is already in character and has no issues, write 'None'. Be direct and concise."
        )
        try:
            path.parent.mkdir(parents=True, exist_ok=True)
            path.write_text(default_critique, encoding="utf-8")
        except OSError:
            pass
        return default_critique
    try:
        return path.read_text(encoding="utf-8")
    except Exception:
        return ""


# ---------------------------------------------------------------------------
# Context retrieval from SessionDB
# ---------------------------------------------------------------------------

def _get_last_user_query(session_id: str) -> str:
    if not session_id:
        return ""
    try:
        from hermes_state import SessionDB
        db = SessionDB()
        messages = db.get_messages(session_id)
        for msg in reversed(messages):
            if msg.get("role") == "user":
                content = msg.get("content") or ""
                if isinstance(content, str):
                    return content
                elif isinstance(content, dict) and "text" in content:
                    return content["text"]
    except Exception as e:
        logger.warning("personality-rewrite: failed to load last user query from DB: %s", e)
    return ""


# ---------------------------------------------------------------------------
# Prompt formatting
# ---------------------------------------------------------------------------

def format_prompt(template: str, identity_anchor: str, slop_rules: str, user_query: str, raw_draft: str, detected_slops: str, critique: str = "") -> str:
    replacements = {
        "{{identity_anchor}}": identity_anchor,
        "{identity_anchor}": identity_anchor,
        "{{slop_rules}}": slop_rules,
        "{slop_rules}": slop_rules,
        "{{user_query}}": user_query,
        "{user_query}": user_query,
        "{{raw_draft}}": raw_draft,
        "{raw_draft}": raw_draft,
        "{{detected_slops}}": detected_slops,
        "{detected_slops}": detected_slops,
        "{{critique}}": critique,
        "{critique}": critique,
    }
    res = template
    for k, v in replacements.items():
        res = res.replace(k, v)
    return res


# ---------------------------------------------------------------------------
# Slop detector
# ---------------------------------------------------------------------------

def _count_and_record_slops(text: str, phrases: list[str], counts: dict[str, int]) -> list[str]:
    detected = []
    text_lower = text.lower()
    for phrase in phrases:
        count = text_lower.count(phrase.lower())
        if count > 0:
            counts[phrase] = counts.get(phrase, 0) + count
            detected.append(phrase)
    return detected


